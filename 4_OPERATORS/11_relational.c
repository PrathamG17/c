#include <stdio.h>

int main(void)
{
	//  < ,  <=
	printf("\n10 < 20 = %d",10 < 20);			//1
	printf("\n20 < 10 = %d",20 < 10);			//0
	printf("\n10 < 10 = %d",10 < 10);			//0
	printf("\n10 <= 10 = %d",10 <= 10);			//1
	
	//  > , >=
	printf("\n\n10 > 20 = %d",10 > 20);			//0
	printf("\n20 > 10 = %d",20 > 10);			//1
	printf("\n10 > 10 = %d",10 > 10);			//0
	printf("\n10 >= 10 = %d",10 >= 10);			//1

	//  == , != 
	printf("\n\n10 == 20 : %d",10 == 20);		//0
	printf("\n10 == 10 : %d",10 == 10);			//1
	printf("\n10 != 10 : %d",10 != 10);			//0
	printf("\n10 != 20 : %d",10 != 20);			//1
	
	printf("\n\n10 <= 10 != 1 : %d",10 <= 10 != 1);		//0
	printf("\n20 >= 10 == 1 : %d",20 >= 10 == 1);		//1
	printf("\n20 < 10 != 0 : %d",20 < 10 != 0);			//0
	
	return 0;
}
